void moveSnake()
{
    Location head = snakeBody.back();
    switch (snakeDirection)
    {
    case UP:
        head.r -= 1;
        break;
    case DOWN:
        head.r += 1;
        break;
    case LEFT:
        head.c -= 1;
        break;
    case RIGHT:
        head.c += 1;
        break;
    }
    if(board[head.r][head.c]==2)
    {
        string sound = "apple sound.wav";
        PlaySound(sound.c_str(),NULL,SND_ASYNC);
        snakeBody.push(head);
        drawSnakeBody(head.r,head.c);
        board[head.r][head.c] = 1;
        counter++;
        generateNextApple();

    }
    else if(board[head.r][head.c]==3 || board[head.r][head.c]==1)
    {
        string sound_LOSE = "mixkit-player-losing-or-failing-2042.wav";
        PlaySound(sound_LOSE.c_str(),NULL,SND_ASYNC);
        gameoverscreen();
    }
    else
    {
        snakeBody.push(head);
        drawSnakeBody(head.r,head.c);
        board[head.r][head.c] = 1;
        Location tail = snakeBody.front();
        snakeBody.pop();
        eraseSnakeBody(tail.r,tail.c);
        board[tail.r][tail.c] = 0;
    }
}

void changeSnakeDriection(char ch)
{
    switch (ch)
    {
    case KEY_UP:
        if(snakeDirection == DOWN)
            ;
        else
            snakeDirection = UP ;
        break;
    case KEY_DOWN:
        if(snakeDirection == UP)
            ;
        else
            snakeDirection = DOWN ;
        break;
    case KEY_LEFT:
        if(snakeDirection == RIGHT)
            ;
        else
            snakeDirection = LEFT ;
        break;
    case KEY_RIGHT:
        if(snakeDirection == LEFT)
            ;
        else
            snakeDirection = RIGHT ;
        break;

    }
}

void checkKeyInput()
{
    char ch;
    if(kbhit())
    {
        ch = getch();
        if (ch == 0)
        {
            ch = getch();
            changeSnakeDriection(ch);
        }
    }
}

